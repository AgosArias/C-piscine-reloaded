int	ft_sqrt(int nb)
{
	int	x;
	int	y;

	x = 1;
	y = 2;
	if (nb < 0)
		return (0);
	while (!((x * x) <= nb && y * y >= nb))
	{
		y++;
		x++;
	}
	if (y * y ==  nb)
		return (y);
	if (x * x == nb)
		return (x);
	return(0);

}
